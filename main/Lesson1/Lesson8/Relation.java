package org.telran.main.Lesson1.Lesson8;

public class Relation {
    public static void main(String[] args) {
        int val1 = 10 , val2 = 15, val3 = -5;

        System.out.println(val1 == val2);
        System.out.println(val1 != val2);
        System.out.println(val1 < val3);
        System.out.println(val2 > val3);

        double myDouble = 10.56;
        System.out.println(Math.round(myDouble));
        // оператор округления
    }
}
